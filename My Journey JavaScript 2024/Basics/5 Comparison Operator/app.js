// Comparison Operators
// == Equal
// != Not Equal
// === Strict Equal
//  !== Strict Not Equal
// > Greater than
// >= Grater than or Equal
// < Less than
// <= Less than or Equal

var a = 10;
var b = 20;

var c = a == b;
var d = a != b;
console.log(c);

// Logical Operators

var a = 10;
var b = 20;

var c = a && b;
var d = a || b;
console.log(c);

// Greater than or equal

var a = 10;
var b = 20;

var c = a >= b;
console.log(c);

// Less than or equal

var a = 10;
var b = 20;

var c = a <= b;
console.log(c);
