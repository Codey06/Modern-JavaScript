// Control FLow
// if and elseif and else

var a = 100;

if (a > 10) {
  console.log("a is greater than 10");
} else {
  console.log("a is less than 10");
}

// elseif

var a = 100;

if (a > 10) {
  console.log("a is greater than 10");
} else if (a > 20) {
  console.log("a is greater than 20");
} else {
  console.log("a is less than 10 or 20");
}

//  switch

var a = 100;

switch (a) {
  case 10:
    console.log("a is 10");
    break;
  case 20:
    console.log("a is 20");
    break;
  case 30:
    console.log("a is 30");
    break;
  default:
    console.log("a is not 10, 20 or 30");
    break;
}

